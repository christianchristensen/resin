package example;

public interface MathService {
  /**
   * Adds two integers.
   */
  public int add(int a, int b);
  
  /**
   * Subtract two integers.
   */
  public int sub(int a, int b);
  
  /**
   * Multiply two integers.
   */
  public int mul(int a, int b);
  
  /**
   * Divide two integers.
   */
  public int div(int a, int b);
}
